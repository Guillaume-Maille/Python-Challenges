import turtle
wn = turtle.Screen()
tess = turtle.Turtle()
tess.right(90)          #tess turns right 90
tess.left(3600)         #tess turns left 3600
tess.right(-90)         #tess turns left 90
tess.speed(10)          #tess speed = 10
tess.left(3600)         #tess turns left 3600
tess.speed(0)           #tess speed is instant
tess.left(3645)         #tess turns left 3945
tess.forward(-100)      #tess goes backwards 100
